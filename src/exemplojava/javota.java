package exemplojava;

public class javota {
	
	 public static void main(String[] args) {
		 char a = 'a';
		 int i = 0;
		 float f = 10.3f;
		 double d = 10;
		 
		 System.out.println("a = " + a);
		 System.out.println("i = " + i);
		 System.out.println("f = " + f);
		 System.out.println("d = " + d);
		 
	 }

}
